if object_id('_countrycode') is not null
	drop table _countrycode;
create table _countrycode(
	code			char(3)			default ''	not null,			-- 三位代码
	name			varchar(40) 	default '' 	not null,			--	中文简称
	ename			varchar(40) 	default '' 	not null,			-- 英文简称
	short			char(2)			default ''	not null,			-- 二位代码
	iso			char(3)			default ''	not null,			-- ISO 码
	allname		varchar(60)		default ''	not null,			-- 中文全称
	allename		varchar(60)		default ''	not null,			-- 英文全称
	area_code	char(10)			default ''	not null,			-- 电话区号
	worldcode_c	varchar(20)		default ''	not null,			-- 所属洲(中)
	worldcode_e	varchar(20)		default ''	not null,			-- 所属洲(英)
	worldcode	char(10)			default ''	not null,			-- 所属区域码(旅游基层月报用)
	postcode		char(1)			default ''	not null				-- 邮件投递码

);

EXEC sp_primarykey '_countrycode', code;
CREATE UNIQUE INDEX index1 ON _countrycode(code);

